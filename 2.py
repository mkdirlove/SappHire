# Taking user input
input1 = float(input("Input 1: "))
input2 = float(input("Input 2: "))

# Operations and printing results
print("Sum:", input1 + input2)
print("Difference:", input1 - input2)
print("Product:", input1 * input2)
print("Modulo:", input1 % input2)

# Increment and decrement using assignment operators
input1 += input2
print("Value after increment:", input1)

input1 -= input2
print("Value after decrement:", input1)
